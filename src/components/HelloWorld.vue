<script setup>
import { ref, computed } from 'vue'

defineProps({
  msg: String
})
let baseConfig = {
  "name": "base",
  "correctSpotEmoji": "游린",
  "correctLetterEmoji": "游리",
  "wrongLetterEmoji": "游릱"
}
let customConfig = {
  "name": "Chicks",
  "correctSpotEmoji": "游냒",
  "correctLetterEmoji": "游냓",
  "wrongLetterEmoji": "游볰"
}

const value = ref('SUTOM #67 3/6游린游릱游리游릱游릱游리游린游리游린游린游린游린游린游릱游리游릱游린游린游린游린游린游린游린游린')
const transform = computed(() => {
  return [...value.value].map(letter => {
    if (letter === ' ') {
      return ' '
    }
    if (letter === baseConfig.correctSpotEmoji) {
      return customConfig.correctSpotEmoji
    }
    if (letter ===  baseConfig.correctLetterEmoji) {
      return customConfig.correctLetterEmoji
    }
    if (letter ===  baseConfig.wrongLetterEmoji) {
      return customConfig.wrongLetterEmoji
    }
    return letter
  }).join('')
  })
</script>

<template>
  <h1>{{ msg }}</h1>
  <textarea rows="10" v-model="value"></textarea>
  <textarea rows="10" v-model="transform"></textarea>
</template>

